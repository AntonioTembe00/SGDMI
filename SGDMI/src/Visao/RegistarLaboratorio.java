    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        // TODO add your handling code here:
        Laboratorio a = new Laboratorio();

        LaboratorioDAO estu = new LaboratorioDAO();

        a.setNumerolaboratorio(jtfNumeroLaboratorio.getText());
        a.setComponentesinformaticos(jtfComponentesInformaticos.getText());
        a.setComponentesnaoinformaticos(jtfComponentesNaoInformaticos.getText());
        a.setData(new Date());

        if (estu.gravar(a)) {
            JOptionPane.showMessageDialog(null, "O laboratorio foi registado com sucesso");
            jtfNumeroLaboratorio.setText(null);
            jtfComponentesInformaticos.setText(null);
            jtfComponentesNaoInformaticos.setText(null);
        } else {
            JOptionPane.showMessageDialog(null, "O laboratorio nao foi  registado com sucesso");
        }

        readJTable();
    }//GEN-LAST:event_jbtGravarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Laboratorio a = new Laboratorio();

            LaboratorioDAO estu = new LaboratorioDAO();

            a.setNumerolaboratorio(jtfNumeroLaboratorio.getText());
            a.setComponentesinformaticos(jtfComponentesInformaticos.getText());
            a.setComponentesnaoinformaticos(jtfComponentesNaoInformaticos.getText());
            a.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));

            if (estu.atualizar(a)) {
                JOptionPane.showMessageDialog(null, "O laboratorio foi actualizado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "O laboratorio nao foi actualizado!!!");
            }

            jtfNumeroLaboratorio.setText(null);
            jtfComponentesInformaticos.setText(null);
            jtfComponentesNaoInformaticos.setText(null);
            readJTable();

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

    private void jbtRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRemoverActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Laboratorio a = new Laboratorio();
            LaboratorioDAO dau = new LaboratorioDAO();

            String d = tabela.getValueAt(tabela.getSelectedRow(), 0)
                    .toString();
            a.setNumerolaboratorio(d);
            dau.remover(a);

            JOptionPane.showMessageDialog(null,
                    "Removido com sucesso");
            readJTable();

        } else {
            JOptionPane.showMessageDialog(null,
                    "Selecione um laboratorio para poder remover");
        }

    }//GEN-LAST:event_jbtRemoverActionPerformed

