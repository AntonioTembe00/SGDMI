    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        LaboratorioDAO dau = new LaboratorioDAO();

        for (Laboratorio a : dau.consular()) {

            modelo1.addRow(new Object[]{a.getNumerolaboratorio(), a.getComponentesinformaticos(),
                a.getComponentesnaoinformaticos(), a.getData()

            });

        }
    }

    public void readJTable1() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        LaboratorioDAO dau = new LaboratorioDAO();

        for (Laboratorio a : dau.consular()) {
            if (a.getNumerolaboratorio().equalsIgnoreCase(jtfNumeroLaboratorio.getText())) {

                modelo1.addRow(new Object[]{a.getNumerolaboratorio(), a.getComponentesinformaticos(),
                    a.getComponentesnaoinformaticos(), a.getData()

                });
            }
        }
    }

