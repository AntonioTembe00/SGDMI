    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        // TODO add your handling code here:
        Docente a = new Docente();

        DocenteDAO estu = new DocenteDAO();

        a.setNome(jtfNome.getText());
        a.setCodigo(Integer.parseInt((jtfCodigo.getText())));
        SalasDocentes saladocente =(SalasDocentes) jcbSala.getSelectedItem();
        a.setSala(saladocente);
        a.setData(new Date());

        if (estu.gravar(a)) {
            JOptionPane.showMessageDialog(null, "O docente foi registado com sucesso");
            jtfNome.setText(null);
            jtfCodigo.setText(null);
            jcbSala.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "O docente nao foi  registado com sucesso");
        }

        readJTable();
    }//GEN-LAST:event_jbtGravarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Docente a = new Docente();

            DocenteDAO estu = new DocenteDAO();

            a.setNome(jtfNome.getText());
            a.setCodigo(Integer.parseInt((jtfCodigo.getText())));
            a.setSala((SalasDocentes) jcbSala.getSelectedItem());
            a.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));

            if (estu.atualizar(a)) {
                JOptionPane.showMessageDialog(null, "O docente foi actualizado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "O docente nao foi actualizado!!!");
            }

            jtfNome.setText(null);
            jtfCodigo.setText(null);
            jcbSala.setSelectedIndex(0);
            readJTable();

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

    private void jbtRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRemoverActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Docente a = new Docente();
            DocenteDAO dau = new DocenteDAO();

            String d = tabela.getValueAt(tabela.getSelectedRow(), 0)
                    .toString();
            a.setCodigo(Integer.parseInt(d));
            dau.remover(a);

            JOptionPane.showMessageDialog(null,
                    "Removido com sucesso");
            readJTable();

        } else {
            JOptionPane.showMessageDialog(null,
                    "Selecione um docente para poder remover");
        }

    }//GEN-LAST:event_jbtRemoverActionPerformed

